package xyz.mrym.applibs.cardviewlib;

public class MyBaseCard {
    private int daoyin_id;
    private String daoyin_name;
    private String daoyin_level;
    private String daoyin_time;
    private String daoyin_hot;

    public MyBaseCard() {
    }

    public int getDaoyin_id() {
        return daoyin_id;
    }

    public void setDaoyin_id(int daoyin_id) {
        this.daoyin_id = daoyin_id;
    }

    public String getDaoyin_name() {
        return daoyin_name;
    }

    public void setDaoyin_name(String daoyin_name) {
        this.daoyin_name = daoyin_name;
    }

    public String getDaoyin_level() {
        return daoyin_level;
    }

    public void setDaoyin_level(String daoyin_level) {
        this.daoyin_level = daoyin_level;
    }

    public String getDaoyin_time() {
        return daoyin_time;
    }

    public void setDaoyin_time(String daoyin_time) {
        this.daoyin_time = daoyin_time;
    }

    public String getDaoyin_hot() {
        return daoyin_hot;
    }

    public void setDaoyin_hot(String daoyin_hot) {
        this.daoyin_hot = daoyin_hot;
    }
}
